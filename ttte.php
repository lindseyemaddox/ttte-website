		<h4>The internationally acclaimed Tennessee Tech Tuba Ensemble will celebrate its 49th anniversary during the academic year 2015/2016.</h4>

		<p>The Tennessee Tech Tuba Ensemble is one of if not the most successful performing collegiate ensembles in history.  Recognized internationally as the leading group of its kind, the TTTE has an enviable record of twenty-seven recording projects, eight Carnegie Hall appearances, two World’s Fairs performances, numerous national and international conference engagements, a forty eight-year history of performances from Preservation Hall in New Orleans to the Spoleto Festival in Charleston to the Kennedy Center in Washington and responsible for the composition and arrangement of more music for the tuba than any other single source.  Founded in 1967 by R. Winston Morris, the TTTE and Morris established and defined the standards for tuba ensemble performance practices and have inspired the formation of like groups all over the world.  The subject of several doctoral dissertations, the TTTE most recently enjoyed wide-spread exposure via a nationally broadcast PBS documentary titled TUBA U: Basso Profundo and the publication of The Tennessee Tech Tuba Ensemble and R. Winston Morris: A 40th Anniversary Retrospective by The Scarecrow Press.</p>

		<p>The TTU group is the only music group in the state of Tennessee to receive the Tennessee Board of Regents "TBR Academic Excellence and Quality Award."  The Ensemble’s greatest legacy, however, lies in the development of dozens of professional music teachers and tuba/euphonium performers who actively teach and perform throughout the world and other members who have pursued and excelled in many other professions.</p>

		<p>Over 300 former members of the TTTE continue to support the group.  For a complete schedule for all activities throughout the year check the tuba studio website under the Department of Music.</p>

